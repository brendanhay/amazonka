extraParam_name :: Lens.Lens' ExtraParam ExtraParamName
extraParam_name = Lens.lens (\ExtraParam' {name} -> name) (\s@ExtraParam' {} a -> s {name = a} :: ExtraParam)

-- | The value that corresponds with the name of an extra parameter.
extraParam_value :: Lens.Lens' ExtraParam Prelude.Text
extraParam_value = Lens.lens (\ExtraParam' {value} -> value) (\s@ExtraParam' {} a -> s {value = a} :: ExtraParam) Prelude.. Data._Sensitive

instance Data.FromJSON ExtraParam where
  parseJSON =
    Data.withObject
      "ExtraParam"
      ( \x ->
          ExtraParam'
            Prelude.<$> (x Data..: "Name")
            Prelude.<*> (x Data..: "Value")
      )

instance Prelude.Hashable ExtraParam where
  hashWithSalt _salt ExtraParam' {..} =
    _salt
      `Prelude.hashWithSalt` name
      `Prelude.hashWithSalt` value

instance Prelude.NFData ExtraParam where
  rnf ExtraParam' {..} =
    Prelude.rnf name `Prelude.seq` Prelude.rnf value

instance Data.ToJSON ExtraParam where
  toJSON ExtraParam' {..} =
    Data.object
      ( Prelude.catMaybes
          [ Prelude.Just ("Name" Data..= name),
            Prelude.Just ("Value" Data..= value)
          ]
      )
